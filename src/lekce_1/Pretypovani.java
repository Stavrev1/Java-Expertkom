package lekce_1;

public class Pretypovani {

	public static void main(String[] args) {
		double a = 10;
		double b = 8;
		
		int c = (int) (a / b);
		System.out.println(c);
		
		float f = (float) (a / b);
		System.out.println(f);
		
		double d = (double) (a / b);
		System.out.println(d);

	}

}
