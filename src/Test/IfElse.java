package Test;

public class IfElse {

	public static void main(String[] args) {
		
		for (int x = 0; x <= 5; x++) {
			if (x == 1)
				System.out.println("x je 1");
			else if (x == 2)
				System.out.println("x je 2");
			else if (x == 3)
				System.out.println("x je 3");
			else 
				System.out.println("x neni 1, 2, 3");
		}

	}

}
